function GetWFashReqWeapon(val){
	var txt="";
		if (val > 0){
		for(var i=0;i<FashWMCombo.length;i++){
			if (FashWMCombo[i][1]==val){
				return FashWMCombo[i][0];
				break;
			}
		}	
		var len=FashWeapMask.length;
		var max;
		var txtArr=[];
		var tai=-1;
		var x;
		for(var i=0;i<len;i++){
			//alert(Math.pow(2, (len-i-1)));
			x=FashWeapMask[len-i-1][1];
			if (x<=val){
				tai++;
				val=val-x;
				txtArr[tai]=FashWeapMask[len-i-1][0];
			}
		}
		if (tai > -1){
			txt=txtArr.join(", ");
		}
		return txt;
	}
	return txt;
}

function GetAddonString(id, val){
	if (StatName[id] != null){
		var ch=StatName[id].substr(0, 2);
		if (id == 30){val=val*100;}
		if (ch == "-%"){
			return (StatName[id].substr(2)+" -"+val+"%");
		}else if (ch == "-."){
			return (StatName[id].substr(2)+" -"+(parseInt(val, 10)*0.05).toFixed(2)+"");
		}else if (ch[0] == "%"){
			return (StatName[id].substr(1)+" +"+val+"%");
		}else if (ch[0] == "^"){
			return (StatName[id].substr(1)+" +"+val+"/sec");
		}else if (ch[0] == ">"){
			return (StatName[id].substr(1)+" +"+val+" m/sec");
		}else if (ch[0] == "?"){
			return (StatName[id].substr(1));
		}else{
			return (StatName[id]+" +"+val);
		}
	}else{
		return ("unknown: "+val);
	}
}